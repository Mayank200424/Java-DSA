package Program;

public class prog7 {
    public static void main(String[] args) {
        //odd Number
        System.out.print("Odd Number");
        for (int i=1;i<=99;i=i+2){
            System.out.println(i);
        }

        //Even Number
        System.out.println("Even Number");
        for(int i=2;i<=100;i=i+2){
            System.out.println(i);
        }
    }
}
