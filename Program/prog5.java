package Program;

public class prog5 {
    public static void main(String[] args) {
        //Reverse Number 1 to 100
        //For Loop
        System.out.println("For Loop");
        for(int i=100; i>=1;i--){
            System.out.println(i);
        }

        //While Loop
        System.out.println("While Loop");
        int i=100;
        while (i >=1){
            System.out.println(i);
            i--;
        }
    }
}
