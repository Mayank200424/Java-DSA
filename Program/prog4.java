package Program;

public class prog4 {
    public static void main(String[] args) {
        //For Loop
        System.out.println("For Loop");
        for(int i=1;i<=100;i++){
            System.out.println(i);
        }

        //While Loop
        System.out.println("While Loop");
        int i = 1;
        while(i <= 100){
            System.out.println(i);
            i++;
        }

    }
}
